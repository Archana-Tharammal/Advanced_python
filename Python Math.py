# min & max functions
x=min(50,2,5)
y=max(54,65,12)
print(x)
print(y)

#abs() function
x=abs(-52.25)
print(x)

#pow(x,y) function
x=pow(2,3)
print(x)

#math module
import math

x=math.sqrt(625)        #square root
print(x)

x=math.ceil(1.3)        #Round a number upward to its nearest integer
y=math.floor(1.3)       #Round a number downward to its nearest integer
print(x)
print(y)

a=math.pi               #returns pi
print(a)